package advanced.lessons.generics;

public class Water extends Liquid {

    String type;

    public Water(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return type;
    }
}
